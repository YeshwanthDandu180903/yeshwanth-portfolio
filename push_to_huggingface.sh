# Commands to Push to Hugging Face Space

# 1. Add Hugging Face as a remote
git remote add huggingface https://huggingface.co/spaces/Yeshu1809/portfolio-backend

# 2. Push your code to Hugging Face
git push huggingface main

# Note: You'll need to authenticate with your Hugging Face access token
# Generate one at: https://huggingface.co/settings/tokens
